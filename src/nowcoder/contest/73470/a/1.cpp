void solve() {
  int n;
  cin >> n;
  set<char> b;
  string s;
  cin >> s;
  int ans = 0;
  for (auto i : s) {
    if (b.find(i) == b.end()) {
      ans += 2;
    } else {
      ans++;
    }
    b.insert(i);
  }
  cout << ans << endl;
}

