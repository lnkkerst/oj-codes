void solve() {
  string s;
  cin >> s;
  sort(s.begin(), s.end());
  if (s[0] == '0') {
    int p = 0;
    while (s[p] == '0') {
      ++p;
    }
    swap(s[p], s[0]);
  }
  cout << s << endl;
}

signed main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);
  int t = 1;
  while (t--) {
    solve();
  }
}
