#define int long long

void solve() {
  int n;
  cin >> n;
  int ans = 0;
  int l = 1, r = 0;
  while (l <= n) {
    r = min(n, n / (n / l));
    ans += (r - l + 1) * (n / l);
    l = r + 1;
  }
  cout << ans << endl;
}

signed main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);
  int t = 1;
  cin >> t;
  while (t--) {
    solve();
  }
}
