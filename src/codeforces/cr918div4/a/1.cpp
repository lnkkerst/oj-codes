void solve() {
  map<int, int> b;
  for (int i = 0; i < 3; ++i) {
    int x;
    cin >> x;
    ++b[x];
  }
  for (auto [k, v] : b) {
    if (v == 1) {
      cout << k << endl;
      return;
    }
  }
}

