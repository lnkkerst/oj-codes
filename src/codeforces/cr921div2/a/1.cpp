void solve() {
  int n, k;
  cin >> n >> k;
  string s;
  for (int j = 1; j <= n; ++j) {
    for (auto i = 'a'; i <= 'a' + k - 1; ++i) {
      s += i;
    }
  }
  cout << s << endl;
}

signed main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);
  int t = 1;
  cin >> t;
  while (t--) {
    solve();
  }
}
